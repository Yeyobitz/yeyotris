const SPAWN_X: Record<TetrominoType, number> = {
  I: 3,
  O: 4,
  T: 3,
  S: 3,
  Z: 3,
  J: 3,
  L: 3,
};

const SPAWN_Y = 0;

export class Tetromino {
  type: TetrominoType;
  x: number;
  y: number;
  rotation: RotationState;

  constructor(type: TetrominoType, x?: number, y?: number, rotation?: RotationState) {
    this.type = type;
    this.x = x ?? SPAWN_X[type];
    this.y = y ?? SPAWN_Y;
    this.rotation = rotation ?? 0;
  }

  get shape(): Position[] {
    return SHAPES[this.type][this.rotation];
  }

  getPositions(x = this.x, y = this.y, rotation: RotationState = this.rotation): Position[] {
    return SHAPES[this.type][rotation].map((p) => ({ x: p.x + x, y: p.y + y }));
  }

  rotate(clockwise: boolean): void {
    if (this.type === 'O') return;
    if (clockwise) {
      this.rotation = ((this.rotation + 1) % 4) as RotationState;
    } else {
      this.rotation = ((this.rotation + 3) % 4) as RotationState;
    }
  }

  clone(): Tetromino {
    return new Tetromino(this.type, this.x, this.y, this.rotation);
  }
}
